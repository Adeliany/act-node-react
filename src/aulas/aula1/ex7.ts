/*
 * Declarando funções
 * Escreva uma função que receba 2 números e informe se o primeiro é maior que o segundo
 *
 * Faça a declaração da função.
 * Troque "{}" pelo nome dado a função
 */
function maior(a: number, b: number) :boolean {
  if (a > b) {
    return true;
  }
  return false;
}

export default maior;
